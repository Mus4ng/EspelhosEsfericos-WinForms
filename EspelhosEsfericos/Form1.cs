        private void btnCalc_Click(object sender, EventArgs e)
        {
            int nulo = 0;

            if (txtFocal.Text == "")
            {
                nulo += 1;
            }
            if (txtObjeto.Text == "")
            {
                nulo += 1;
            }
            if (txtImagem.Text == "")
            {
                nulo += 1;
            }


            if (nulo >= 2)
            {
                MessageBox.Show("São necessários no mínimo dois valores para que o cálculo seja realizado!", "Aviso!", MessageBoxButtons.OK, MessageBoxIcon.Exclamation);
            }

        }

